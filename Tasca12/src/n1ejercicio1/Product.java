package n1ejercicio1;

public class Product {

	public String name;
	public float price;

	public Product(String name, float price) {
		super();
		this.name = name;
		this.price = price;
	}

	public float getPrice() {
		return price;
	}

	public void setPrice(float price) {
		this.price = price;
	}

}
