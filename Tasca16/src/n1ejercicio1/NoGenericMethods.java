package n1ejercicio1;

public class NoGenericMethods {

	private int uno;
	private int dos;
	private int tres;

	public NoGenericMethods(int uno, int dos, int tres) {
		super();
		this.uno = uno;
		this.dos = dos;
		this.tres = tres;
	}

	@Override
	public String toString() {
		return "NoGenericMethods [uno=" + uno + ", dos=" + dos + ", tres=" + tres + "]";
	}

}
